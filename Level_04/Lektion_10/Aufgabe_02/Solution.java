package Level_04.Lektion_10.Aufgabe_02;
/* 
Von 10 bis 1
*/

public class Solution {
    public static void main(String[] args) throws Exception {
        //schreib hier deinen Code
        int start = 10, ende = 1;
        while(start >= ende){
            System.out.println(start);
            start--;
        }

    }
}